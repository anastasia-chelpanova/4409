#include "Ways.hpp"

// Рекурсивная функция для подсчёта количества способов подняться по лестнице
// ways(n) = ways(n-1) + ways(n-2)
// Базовые случаи: ways(0) = 1, ways(1) = 1
int CountWays(int n) 
{
    // Некорректное значение — 0 способов
    if (n < 0) {
        return 0;
    }
    // Базовые случаи (рекурсия останавливается здесь)
    if (n == 0 || n == 1) {
        return 1;
    }
    // Рекурсивный шаг: количество способов = 
    // способы дойти до n-1 + способы дойти до n-2
    return CountWays(n - 1) + CountWays(n - 2);
}
