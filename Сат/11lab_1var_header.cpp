// Функция вычисления суммы ряда ln(1+x)
// Ряд: x - x^2/2 + x^3/3 - x^4/4 + ... + (-1)^(n+1) * x^n / n

double raschet(double x, int n, double &Q) {
    double S = 0;        // сумма ряда (инициализируем 0)
    double a;            // текущее слагаемое

    // Цикл for от 1 до n (включительно)
    // i++ - увеличение i на 1 после каждой итерации
    for (int i = 1; i <= n; i++) {
        // Вычисляем i-е слагаемое: (-1)^(i+1) * x^i / i
        // Определяем знак: для нечетных i знак +, для четных -
        // % - оператор взятия остатка от деления
        if (i % 2 == 1) {            // если i нечетное (остаток 1)
            a = pow(x, i) / i;        // положительное слагаемое
        } else {                       // если i четное (остаток 0)
            a = -pow(x, i) / i;        // отрицательное слагаемое
        }

        S += a;                        // добавляем к сумме (S = S + a)

        // Если это последнее слагаемое (i == n), сохраняем его в Q (передано по ссылке)
        if (i == n) {
            Q = a;
        }
    }

    return S;    // возвращаем сумму
}

